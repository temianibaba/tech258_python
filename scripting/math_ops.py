# Math operators
# This is a module
# A package is multiple modules into one.
# A library is the biggest one, it refers to packages and modules
def add(int1, int2):
    return int1 + int2

def subtract(int1, int2):
    return int1 - int2

def multiply(int1, int2):
    return int1 * int2

def divide(int1, int2):
    return int1 / int2
